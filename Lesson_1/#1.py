# 1 Арифметичні операції:
# Написати програму, яка зчитує два числа та виводить їхню суму, різницю, добуток та частку.
var_1 = int(input("Enter the number #1: "))
var_2 = int(input("Enter the number#2: "))

result_1 = (var_1 + var_2)
result_2 = (var_1 - var_2)
result_3 = (var_1 / var_2)
result_4 = (var_1 * var_2)

print("Sum: ",result_1)
print("Deduction: ", result_2)
print("Division: ",result_3)
print("Multiplicatin: ",result_4)
